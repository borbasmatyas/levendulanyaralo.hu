
<article>
  <p>
    Orfű központjában az Ady Endre utcában található a levendula vendégház.
    Közelében  néhány perces sétával  orvosi rendelő, posta, élelmiszer bolt és éttermek találhatóak.
    Nyugodt, csendes környezet 600 m<sup>2</sup>-es kerttel.
  </p>

  <p>
    Az Orfűi-tó-tól 1 percre ahol napi jeggyel horgászni is lehet. A 2011-ben elkészült Aquapark megközelíthető - autóval 5 perc vagy a tóparti sétányon gyönyörű környezetben kényelmesen 30 perc.
    A nyaraló előtt halad el a Pécs-Orfű kerékpárút amin kb: 90 perc alatt elérhető Pécs. 
  </p>

  <p>
    Felszereltsége: 30 m<sup>2</sup>-es nyaraló, 2 felnőtt + max. 3 gyerek részére. Egylégterű szoba 1 franciaággyal és 1 emeletes ággyal. Fürdőszoba - zuhanyzóval.
    A kertben hinta, kerti-sütő, kerti zuhanyzó.
  </p>

  <p>
    Széchenyi-pihenőkártyával fizethető a szállás díja.
  </p>

  <p>
    Kisállat nem vihető be.
  </p>


